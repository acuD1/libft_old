#include "unistd.h"

int		ft_atoi(const char *s)
{
	size_t	i;
	int		sign;
	int		nb;

	i = 0;
	nb = 0;
	sign = 1;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '+' || s[i] == '-')
		sign *= (s[i++] == '-' ? -1 : 1);
	while (s[i] >= '0' && s[i] <= '9')
		nb = (nb * 10) + (s[i++] - '0');
	return (sign * nb);
}
