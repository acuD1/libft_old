#include "libft.h"

void	ft_lstrev(t_list **alst)
{
	t_list	*current;
	t_list	*next;
	t_list	*prev;

	current = *alst;
	next = NULL;
	prev = NULL;
	while (current)
	{
		next = current->next;
		current->next = prev;
		prev = current;
		current = next;
	}
	*alst = prev;
}
